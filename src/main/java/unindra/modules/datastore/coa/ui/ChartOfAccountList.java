    public void loadData() {
        CoaService service = new CoaService();
        String search = fSearch.getText();
        List<Coa> data = service.getCoa(search); 
        DefaultTableModel model = (DefaultTableModel) coaTable.getModel();
        model.setRowCount(0); 

        for (Coa coa : data) {
            model.addRow(new Object[]{
                coa.getCode(),
                coa.getName()
            });
        }
    }
