    public void setCallback(Consumer<Contact> callback) {
        this.callback = callback;
        title.setText("Pilih Kontak");
    }

    public void loadData() {
        Object[] headers = {"Nama", "Telp", "Email", "Alamat"};
        DefaultTableModel model = new DefaultTableModel(null, headers);
        List<Contact> list = ContactService.find(fSearch.getText());
        for (Contact data : list) {
            model.addRow(new Object[]{
                data.getName(), 
                data.getPhone(), 
                data.getEmail(), 
                data.getAddress()
            });
        }
        dataTable.setModel(model);
    }

    private void search() {
        bEdit.setVisible(false);
        bDelete.setVisible(false);
        bCancel.setVisible(true);
        fSearch.setVisible(false);
        bSearch.setVisible(false);
        bCreate.setVisible(false);
        loadData();
    }

    private void reset() {
        bEdit.setVisible(false);
        bDelete.setVisible(false);
        bCancel.setVisible(false);
        fSearch.setVisible(true);
        bSearch.setVisible(true);
        bCreate.setVisible(true);
    }

    private void cancel() {
        fSearch.setText("");
        reset();
        loadData();
    }

    private void choose() {
        if (callback!=null) {
            int selectedRow = dataTable.getSelectedRow();
            if (selectedRow != -1) {
                callback.accept(ContactService.find("").get(selectedRow));
                dispose();
            }
        } else {            
            bEdit.setVisible(true);
            bDelete.setVisible(true);
            bCancel.setVisible(true);
            fSearch.setVisible(false);
            bSearch.setVisible(false);
            bCreate.setVisible(false);
        }
    }

    private void create() {
        new ContactForm(this, null).setVisible(true);
    }

    private void edit() {
        int selectedRow = dataTable.getSelectedRow();
        if (selectedRow != -1) {
            Contact data = ContactService.find("").get(selectedRow);
            new ContactForm(this, data).setVisible(true);
        }
    }

    private void delete() {
        int selectedRow = dataTable.getSelectedRow();
        if (selectedRow != -1) {
            Contact data = ContactService.find("").get(selectedRow);
            int confirm = JOptionPane.showConfirmDialog(this, "Apakah kamu yakin akan menghapus data kontak "+data.getName()+"?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                ContactService.delete(data.getId());
                loadData(); // Refresh the data after deletion
            }
        }
    }

