    public BillForm(BillList list, Transaction data) {
        initComponents();
        pack();
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension windowSize = getSize();
        setLocation((screenSize.width - windowSize.width) / 2, (screenSize.height - windowSize.height) / 2);
        this.list = list;
        if (data != null) {
            title.setText("Edit Tagihan");
            selectedData = data;
            projectId = data.getProjectId();
            fProjectNumber.setText(data.getProjectNumber());
            contactId = data.getContactId();
            fContactName.setText(data.getContactName());
            originCoaId = data.getOriginCoaId();
            targetCoaId = data.getTargetCoaId();
            fTargetCoaName.setText(data.getTargetCoaName());
            fNumber.setText(data.getTrxNumber());
            fDate.setDate(Date.from(data.getTrxDate().atStartOfDay(ZoneId.systemDefault()).toInstant()));
            fDescription.setText(data.getDescription());
            fAmount.setText(data.getAmount().toPlainString());
        } else {
            Date now = new Date();
            projectId = 1;
            fDate.setDate(now);
            fNumber.setText(String.valueOf(now.getTime() / 1000));
        }
    }

    private void lookupProject() {
        ProjectList.openLookup(data -> {
            if (data != null) {
                projectId = data.getId();
                fProjectNumber.setText(data.getNumber());
            }
        });
    }

    private void lookupContact() {
        ContactList.openLookup(data -> {
            if (data != null) {
                contactId = data.getId();
                fContactName.setText(data.getName());
                originCoaId = data.getBillCoaId();
                if (originCoaId == 0) {
                    Coa c = new Coa();
                    c.setCode(CoaService.nextCodeByParentId(2));
                    c.setName("Utang Kepada " + data.getName());
                    c.setParentId(2);
                    c.setCategoryId(2);
                    c.setIsCash(false);
                    originCoaId = CoaService.create(c);
                }
            }
        });
    }

    private void lookupTargetCoa() {
        CoaList.openLookup(coa -> {
            if (coa != null) {
                targetCoaId = coa.getId();
                fTargetCoaName.setText(coa.getName());
            }
        }, "child", 5);
    }

    private void save() {
        Transaction data = new Transaction();
        data.setTrxType("bill");
        data.setProjectId(projectId);
        data.setContactId(contactId);
        data.setOriginCoaId(originCoaId);
        data.setTargetCoaId(targetCoaId);
        data.setTrxNumber(fNumber.getText());
        data.setDescription(fDescription.getText());
        data.setTrxDate(fDate.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
        try {
            BigDecimal amount = new BigDecimal(fAmount.getText());
            data.setAmount(amount);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Nilai anggaran harus berupa angka.");
        }

        if (selectedData == null) {
            TransactionService.create(data);
        } else {
            data.setId(selectedData.getId());
            TransactionService.update(data);
        }
        list.loadData();
        dispose();
    }

