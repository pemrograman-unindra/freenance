    public CoaForm(CoaList coaList, Coa coa) {
        initComponents();
        pack();
        setLocationRelativeTo(null);
        this.coaList = coaList;
        fIsCash.setVisible(false);
        if (coa != null) {
            title.setText("Edit Data Kategori Keuangan");
            selectedCoa = coa;
            fCode.setText(coa.getName());
            fName.setText(coa.getName());
            fParent.setText(coa.getName());
            if (coa.getCategoryId()==1) {
                fIsCash.setVisible(true);
            }
        }
    }

    private void setCategoryId() {
        categoryId = fClass.getSelectedIndex();
        if (categoryId == 1) {
            fIsCash.setVisible(true);
        } else {
            fIsCash.setVisible(false);
        }
    }

    private void lookupParent() {
        CoaList.openLookup(coa -> {
            if (coa != null) {
                CoaService service = new CoaService();
                parentId = coa.getId();
                fParent.setText(coa.getName());
                fCode.setText(String.valueOf(service.nextCodeByParentId(parentId)));
                fName.requestFocus(true);
            }
        });
    }

    private void save() {
        Coa data = new Coa();
        data.setName(fCode.getText());
        data.setName(fName.getText());
        data.setParentId(parentId);

        CoaService service = new CoaService();
        if (selectedCoa == null) {
            service.create(data);
        } else {
            data.setId(selectedCoa.getId());
            service.update(data);
        }
        coaList.loadData();
        dispose();
    }
